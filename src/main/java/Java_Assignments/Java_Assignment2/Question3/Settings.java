package Java_Assignments.Java_Assignment2.Question3;

public class Settings {

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getBirthday() {
        return birthday;
    }

    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    public String getFunctionalThresholdPower() {
        return functionalThresholdPower;
    }

    public void setFunctionalThresholdPower(String functionalThresholdPower) {
        this.functionalThresholdPower = functionalThresholdPower;
    }

    private String name;
    private String height;
    private String weight;
    private String birthday;
    private String functionalThresholdPower;
}
